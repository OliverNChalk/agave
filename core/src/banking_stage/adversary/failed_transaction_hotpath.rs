    //
    // The normal mode execution is a verification that the setup is actually working in the first
    // place.
    //
    // Failed transaction hotpath is expected to update just the payer balance, and not perform the
    // actual transfer.
    #[test_case(true)]
    #[test_case(false)]
    fn one_transfer_transaction(use_failed_transaction_hotpath: bool) {
        let mut setup_config = SimpleSetupConfig::new(100_000);
        let actor1 = setup_config.add_empty_account(7_000);

        let setup = setup_config.construct();
        let SimpleSetupControls {
            bank,
            mint_keypair,
            consumer,
        } = setup.get_controls();

        let actor1_pubkey = actor1.pubkey();
        let mint_pubkey = mint_keypair.pubkey();

        let mint_balance_before = bank.get_balance(&mint_pubkey);
        let actor1_balance_before = bank.get_balance(&actor1_pubkey);

        let tx = transfer_tx_with_budget(
            &mint_keypair,
            &mint_pubkey,
            &actor1_pubkey,
            1_000,
            bank.last_blockhash(),
        );

        // This is expected to call the
        // `failed_transaction_hotpath::process_and_record_aged_transactions()` internally.
        consumer.process_and_record_aged_transactions(
            &bank,
            use_failed_transaction_hotpath,
            &[tx],
            &[MaxAge::MAX; 1],
        );

        setup.shutdown();

        let mint_balance_after = bank.get_balance(&mint_pubkey);
        let actor1_balance_after = bank.get_balance(&actor1_pubkey);

        assert_eq!(
            mint_balance_after,
            mint_balance_before.saturating_sub(bank.fee_structure().lamports_per_signature),
        );
        assert_eq!(actor1_balance_before, actor1_balance_after);
    }

    // This test is very similar to `one_transfer_transaction`, but uses two signature in the
    // transaction, so we expect a different different payment compared to the
    // `one_transfer_transaction`.
    #[test]
    fn two_transfers_transaction() {
        let mut setup_config = SimpleSetupConfig::new(100_000);
        let actor1 = setup_config.add_empty_account(8_000);
        let actor2 = setup_config.add_empty_account(9_000);

        let setup = setup_config.construct();
        let SimpleSetupControls {
            bank,
            mint_keypair,
            consumer,
        } = setup.get_controls();

        let actor1_pubkey = actor1.pubkey();
        let actor2_pubkey = actor2.pubkey();
        let mint_pubkey = mint_keypair.pubkey();

        let mint_balance_before = bank.get_balance(&mint_pubkey);
        let actor1_balance_before = bank.get_balance(&actor1_pubkey);
        let actor2_balance_before = bank.get_balance(&actor2_pubkey);

        let tx = Transaction::new_signed_with_payer(
            &[
                ComputeBudgetInstruction::set_compute_unit_limit(25_000),
                system_instruction::transfer(&mint_pubkey, &actor1_pubkey, 5_000),
                system_instruction::transfer(&actor1_pubkey, &actor2_pubkey, 7_500),
            ],
            Some(&mint_pubkey),
            &[&mint_keypair, &actor1],
            bank.last_blockhash(),
        );
        let tx = RuntimeTransaction::from_transaction_for_tests(tx);
        let tx_required_signature_count = tx.message().header().num_required_signatures;

        let use_failed_transaction_hotpath = true;
        // This is expected to call the
        // `failed_transaction_hotpath::process_and_record_aged_transactions()` internally.
        consumer.process_and_record_aged_transactions(
            &bank,
            use_failed_transaction_hotpath,
            &[tx],
            &[MaxAge::MAX; 1],
        );

        setup.shutdown();

        let mint_balance_after = bank.get_balance(&mint_pubkey);
        let actor1_balance_after = bank.get_balance(&actor1_pubkey);
        let actor2_balance_after = bank.get_balance(&actor2_pubkey);

        let expected_required_signature_count = 2;
        assert_eq!(
            expected_required_signature_count,
            tx_required_signature_count
        );
        assert_eq!(
            mint_balance_after,
            mint_balance_before.saturating_sub(
                u64::from(expected_required_signature_count)
                    * bank.fee_structure().lamports_per_signature
            ),
        );
        assert_eq!(actor1_balance_before, actor1_balance_after);
        assert_eq!(actor2_balance_before, actor2_balance_after);
    }

    // This test verifies that the failed transaction hotpath is correctly overestimating the
    // transaction costs, and would not produce a block that would be invalid when actually
    // replayed.
    #[test]
    fn fully_packed_block() {
        let tx_count_expected_to_fit = 10;
        let tx_count_expected_to_be_throttled = 5;
        let tx_count = tx_count_expected_to_fit + tx_count_expected_to_be_throttled;

        // Failed transaction hostpath is only estimating the transaction costs, so it should be
        // using this value as the cost, when determining if the block is full.
        let tx_cost_estimate = {
            let actor1 = Keypair::new();
            let tx = transfer_tx_with_budget(
                &actor1,
                &actor1.pubkey(),
                &Pubkey::default(),
                1_000,
                Hash::default(),
            );
            CostModel::calculate_cost(&tx, &FeatureSet::all_enabled()).sum()
        };

        // Set the block cost limits to fit only the `tx_count_expected_to_fit` transactions, if
        // their cost is only estimated.
        fn set_bank_cost_limits(bank: &Bank, tx_cost_estimate: u64, tx_count_expected_to_fit: u64) {
            let mut cost_tracker = bank.write_cost_tracker().unwrap();
            let block_cost_limit = tx_cost_estimate * tx_count_expected_to_fit;
            // While normally the `account_cost_limit` and the `vote_cost_limit` are lower than the
            // `block_cost_limit`, for the purposes of this test it is OK for them to be equal.
            cost_tracker.set_limits(block_cost_limit, block_cost_limit, block_cost_limit);
        }

        // Runs `tx_count` transactions, and returns execution stats and the bank.
        let run_one_block =
            |use_failed_transaction_hotpath: bool| -> (ProcessTransactionBatchOutput, Arc<Bank>) {
                let mut setup_config = SimpleSetupConfig::new(100_000);
                // Make sure the accounts in different transactions do not overlap.
                let actors = setup_config.add_n_empty_accounts(2 * tx_count, 35_000);
                let actor_pubkeys = actors
                    .iter()
                    .map(|actor| actor.pubkey())
                    .collect::<Vec<_>>();

                let setup = setup_config.construct();
                let SimpleSetupControls { bank, consumer, .. } = setup.get_controls();

                set_bank_cost_limits(&bank, tx_cost_estimate, tx_count_expected_to_fit);

                let txs = (0..usize::try_from(tx_count).unwrap())
                    .map(|i| {
                        transfer_tx_with_budget(
                            &actors[2 * i],
                            &actor_pubkeys[2 * i],
                            &actor_pubkeys[2 * i + 1],
                            1_000,
                            bank.last_blockhash(),
                        )
                    })
                    .collect::<Vec<_>>();

                let processing_result = consumer.process_and_record_aged_transactions(
                    &bank,
                    use_failed_transaction_hotpath,
                    &txs,
                    &iter::repeat_n(MaxAge::MAX, txs.len()).collect::<Vec<_>>(),
                );

                setup.shutdown();

                (processing_result, bank)
            };

        // In the failed transaction hotpath case we expect the bank to exhaust the block cost.
        let (processing_result, hotpath_case_bank) =
            run_one_block(/* use_failed_transaction_hotpath: */ true);

        assert_eq!(
            processing_result
                .execute_and_commit_transactions_output
                .transaction_counts
                .processed_count,
            tx_count_expected_to_fit,
        );
        assert_eq!(
            tx_count_expected_to_be_throttled,
            processing_result.cost_model_throttled_transactions_count,
        );

        let hotpath_block_cost = hotpath_case_bank.read_cost_tracker().unwrap().block_cost();
        assert_eq!(
            hotpath_block_cost,
            tx_cost_estimate * tx_count_expected_to_fit
        );

        // In the normal case, due to the transaction cost being recalculated based on the actual
        // execution price, expect the bank to have a much lower block cost accumulated from the
        // same set of transactions.
        //
        // QosService will still block transaction execution based on the cost estimate, rather than
        // the execution cost.
        let (processing_result, normal_case_bank) =
            run_one_block(/* use_failed_transaction_hotpath: */ false);

        assert_eq!(
            processing_result
                .execute_and_commit_transactions_output
                .transaction_counts
                .processed_count,
            tx_count_expected_to_fit,
        );
        assert_eq!(
            tx_count_expected_to_be_throttled,
            processing_result.cost_model_throttled_transactions_count,
        );

        let normal_block_cost = normal_case_bank.read_cost_tracker().unwrap().block_cost();
        assert!(
            normal_block_cost + tx_cost_estimate < hotpath_block_cost,
            "While transactions are added to the bank based on the estimates, we expect the \
             failed transaction hot path to use estimates for the actual cost as well.\nAs a \
             result, in the normal processing case we should still have enough space to add at \
             least one more transaction, compared to the hotpath case.\nFailed transaction \
             hotpath bank cost for {tx_count_expected_to_fit} txs: {hotpath_block_cost}\nNormal \
             flow bank cost for {tx_count_expected_to_fit} txs: {normal_block_cost}\nEstimate \
             transaction cost: {tx_cost_estimate}
            "
        );
    }
