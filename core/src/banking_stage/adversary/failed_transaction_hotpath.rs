/// Simplified recording and committing without execution. Will only work correctly for
/// transactions that are guaranteed to fail.
///
/// This function will still run through QoS to ensure we keep within block limits.
///
/// See `banking_stage::consumer::Consumer::process_and_record_aged_transactions()`, and
/// `process_and_record_transactions_with_pre_results()` in the same module, for reference.
pub fn process_and_record_aged_transactions(
    qos_service: &QosService,
    transaction_recorder: &TransactionRecorder,
    committer: &Committer,
    bank: &Bank,
    transactions: &[impl TransactionWithMeta],
    _max_ages: &[MaxAge],
) -> ProcessTransactionBatchOutput {
    // `Consumer::process_and_record_aged_transactions()` is verifying transactions against the
    // `max_ages` here.  This is due to the fact, that transactions are pre-verified when they are
    // received.  And a considerable amount of time might have passed since that point and when they
    // are processed here.
    //
    // But in case of the failed transaction hotpath, all of transactions are generated by the
    // internal generators, we do not expect them to fail the age validation.  This could actually
    // fail in certain edge cases, probably causing cluster divergence.

    let ((transaction_qos_cost_results, cost_model_throttled_transactions_count), cost_model_us) =
        measure_us!(qos_service.select_and_accumulate_transaction_costs(
            bank,
            transactions,
            // `Consumer::process_and_record_aged_transactions()` passes the `max_age` check results
            // here, but we assume all transaction pass it.
            std::iter::repeat(Ok(())),
        ));

    // Only lock accounts for those transactions are selected for the block;
    // Once accounts are locked, other threads cannot encode transactions that will modify the
    // same account state
    //
    // Transaction generators are producing explicit worker thread indices for all transactions, and
    // thus should avoid overlaps, making locking unnecessary.  But, in practice, this may still
    // fail, as the generators are racing against each other, and certain patterns, or bugs, could
    // produce overlaps.  As this verification is supposed to be fast, and the call is constructing
    // a `TransactionBatch` instance that we need later, we keep this call as is.
    let (batch, lock_us) = measure_us!(bank.prepare_sanitized_batch_with_results(
        transactions,
        transaction_qos_cost_results.iter().map(|r| match r {
            Ok(_cost) => Ok(()),
            Err(err) => Err(err.clone()),
        })
    ));

    let execute_and_commit_transactions_output =
        execute_and_commit_failed_transactions(transaction_recorder, committer, bank, &batch);

    // Once the accounts are new transactions can enter the pipeline to process them
    let (_, unlock_us) = measure_us!(drop(batch));

    // We do not want the `QosService` to actually update the execution costs, as they would be
    // incorrect in our case.  We we skip loading of the accounts, the CUs we estimated will be
    // below the correct ones, produced during an actual execution, allowing the leader to pack more
    // transactions into the block than what a replay stage will.
    //
    // Instead we go the opposite route.  `QosService::select_and_accumulate_transaction_costs()`
    // has already added the full CU estimate from the transaction, and
    // `QosService::remove_or_update_costs()` is supposed to refund the CUs that were not used.
    //
    // By skipping the refund, we make sure the leader is more conservative than the replay,
    // producing under-packed blocks.  But as these failed transactions are generally attacking some
    // resource sensitive parts of the system, it is unlikely we really need to pack a full block
    // to produce a desired effect.
    //
    // We still need to reduce the in flight transaction counter, which is normally called inside of
    // the `QosService::remove_or_update_costs()` call.
    //
    // QosService::remove_or_update_costs(
    //     transaction_qos_cost_results.iter(),
    //     execute_and_commit_transactions_output
    //         .commit_transactions_result
    //         .as_ref()
    //         .ok(),
    //     bank,
    // );
    {
        // This is what `QosService::remove_or_update_recorded_transaction_costs()` does.
        let mut cost_tracker = bank.write_cost_tracker().unwrap();
        cost_tracker.sub_transactions_in_flight(
            transaction_qos_cost_results
                .iter()
                .filter(|&res| res.is_ok())
                .count(),
        );
    }

    // Computing accurate CUs without execution is non-trivial.  And as it is only needed for
    // metrics, we skip this step here.
    //
    // let (cu, us) =
    //     Self::accumulate_execute_units_and_time(&execute_and_commit_timings.execute_timings);
    // self.qos_service.accumulate_actual_execute_cu(cu);
    // self.qos_service.accumulate_actual_execute_time(us);

    qos_service.report_metrics(bank.slot());

    debug!(
        "failed_transaction_hotpath: {} lock: {}us unlock: {}us txs_len: {}",
        bank.slot(),
        lock_us,
        unlock_us,
        transactions.len(),
    );

    ProcessTransactionBatchOutput {
        cost_model_throttled_transactions_count,
        cost_model_us,
        execute_and_commit_transactions_output,
    }
}

/// Roughly matches the functionality of the
/// `Consumer::execute_and_commit_transactions_locked()`.  We avoid the actual execution of these
/// transactions, mostly just the commit part is here.
fn execute_and_commit_failed_transactions(
    transaction_recorder: &TransactionRecorder,
    committer: &Committer,
    bank: &Bank,
    batch: &TransactionBatch<impl TransactionWithMeta>,
) -> ExecuteAndCommitTransactionsOutput {
    let mut execute_and_commit_timings = LeaderExecuteAndCommitTimings::default();

    // `Consumer::execute_and_commit_transactions_locked()` will collect balances here for any
    // tokens invoked in the transaction.  This causes the token accounts and the mint accounts to
    // be loaded.  Which we want to avoid for the hotpath in the first place.
    //
    // So we are going to skip the balance updates.  They are relevant for certain RPC requests.
    // But considering we know that all the transactions are going to fail, we may only have
    // incorrect balances for the transaction payer accounts, as a result, if at all.

    let min_max = batch
        .sanitized_transactions()
        .iter()
        .filter_map(|transaction| {
            transaction
                .compute_budget_instruction_details()
                .sanitize_and_convert_to_compute_budget_limits(&bank.feature_set)
                .ok()
                .map(|limits| limits.compute_unit_price)
        })
        .minmax();
    let (min_prioritization_fees, max_prioritization_fees) =
        min_max.into_option().unwrap_or_default();

    let mut error_counters = TransactionErrorMetrics::default();
    // For the moment, failed transaction hotpath logic assumes that all the passed transactions
    // are going to fail the compute budget limits.
    //
    // It is important for the transaction to actually do that.
    //
    // TODO It would be great to actually have a check here, that would verify that the first
    // transaction instruction is a `ComputeBudgetInstruction::set_compute_unit_limit()`
    // instruction.  We should probably also check the argument and make sure it is at or below
    // a certain known small value.  For the write program attack we are using 25_000, so maybe
    // it is a good limit?

    // `execute_and_commit_transactions_locked()` invokes
    // `bank.load_and_execute_transactions_output()` here, but we just assume we know the
    // loading and the execution results already.
    let (load_and_execute_transactions_output, load_execute_us) =
        measure_us!(load_and_execute_failed_transaction(
            bank,
            batch,
            MAX_PROCESSING_AGE,
            &mut execute_and_commit_timings.execute_timings,
            &mut error_counters
        ));
    let LoadAndExecuteTransactionsOutput {
        processing_results,
        processed_counts,
        balance_collector,
    } = load_and_execute_transactions_output;
    execute_and_commit_timings.load_execute_us = load_execute_us;

    let transaction_counts = LeaderProcessedTransactionCounts {
        processed_count: processed_counts.processed_transactions_count,
        processed_with_successful_result_count: processed_counts
            .processed_with_successful_result_count,
        attempted_processing_count: processing_results.len() as u64,
    };

    let (processed_transactions, processing_results_to_transactions_us) =
        measure_us!(processing_results
            .iter()
            .zip(batch.sanitized_transactions())
            .filter_map(|(processing_result, tx)| {
                if processing_result.was_processed() {
                    Some(tx.to_versioned_transaction())
                } else {
                    None
                }
            })
            .collect_vec());

    let (freeze_lock, freeze_lock_us) = measure_us!(bank.freeze_lock());
    execute_and_commit_timings.freeze_lock_us = freeze_lock_us;

    let (record_transactions_summary, record_us) =
        measure_us!(transaction_recorder.record_transactions(bank.slot(), processed_transactions));
    execute_and_commit_timings.record_us = record_us;

    let RecordTransactionsSummary {
        result: record_transactions_result,
        record_transactions_timings,
        starting_transaction_index,
    } = record_transactions_summary;
    execute_and_commit_timings.record_transactions_timings = RecordTransactionsTimings {
        processing_results_to_transactions_us: Saturating(processing_results_to_transactions_us),
        ..record_transactions_timings
    };

    if let Err(recorder_err) = record_transactions_result {
        // Unlike `execute_and_commit_transactions_locked()` we are not going to retry any of
        // these transactions, as we assume it is the generator fault for them to fail in the
        // first place.
        return ExecuteAndCommitTransactionsOutput {
            transaction_counts,
            retryable_transaction_indexes: vec![],
            commit_transactions_result: Err(recorder_err),
            execute_and_commit_timings,
            error_counters,
            min_prioritization_fees,
            max_prioritization_fees,
        };
    }

    let (commit_time_us, commit_transaction_statuses) =
        if processed_counts.processed_transactions_count != 0 {
            committer.commit_transactions(
                batch,
                processing_results,
                starting_transaction_index,
                bank,
                balance_collector,
                &mut execute_and_commit_timings,
                &processed_counts,
            )
        } else {
            (
                0,
                vec![
                    CommitTransactionDetails::NotCommitted(TransactionError::CommitCancelled);
                    processing_results.len()
                ],
            )
        };

    drop(freeze_lock);

    debug!(
        "failed_transaction_hotpath: {} execute_and_commit_failed_transactions: {}us record: {}us \
         commit: {}us txs_len: {}",
        bank.slot(),
        load_execute_us,
        record_us,
        commit_time_us,
        batch.sanitized_transactions().len(),
    );

    ExecuteAndCommitTransactionsOutput {
        transaction_counts,
        retryable_transaction_indexes: vec![],
        commit_transactions_result: Ok(commit_transaction_statuses),
        execute_and_commit_timings,
        error_counters,
        min_prioritization_fees,
        max_prioritization_fees,
    }
}

// This is a replacement for `bank.load_and_execute_transactions()`, but specialized for our
// special kind of transactions, that are known to fail and are only going to update the payer
// account.
fn load_and_execute_failed_transaction(
    bank: &Bank,
    batch: &TransactionBatch<impl TransactionWithMeta>,
    max_age: usize,
    timings: &mut ExecuteTimings,
    error_metrics: &mut TransactionErrorMetrics,
) -> LoadAndExecuteTransactionsOutput {
    let sanitized_txs = batch.sanitized_transactions();

    let balance_collector = Some(BalanceCollector::new_with_transaction_count(
        sanitized_txs.len(),
    ));

    let (check_results, check_us) = measure_us!(bank.check_transactions(
        sanitized_txs,
        batch.lock_results(),
        max_age,
        error_metrics,
    ));
    timings.saturating_add_in_place(ExecuteTimingType::CheckUs, check_us);

    let mut processed_transactions_count = 0;
    let mut processed_non_vote_transactions_count = 0;
    let mut signature_count = 0;

    let processing_results = izip!(sanitized_txs, check_results)
        .map(|(tx, check_result)| {
            let SVMTransactionExecutionAndFeeBudgetLimits {
                budget: compute_budget,
                loaded_accounts_data_size_limit,
                fee_details,
            } = check_result?.compute_budget_and_limits?;

            let tx = tx.as_sanitized_transaction();
            let message = tx.message();

            let fee_payer = message.fee_payer();

            let mut fee_payer_account = bank
                .accounts()
                .load_with_fixed_root(&bank.ancestors, fee_payer)
                .ok_or(TransactionError::AccountNotFound)
                .inspect_err(|_| error_metrics.account_not_found += 1)?
                .0;

            let fee = fee_details.total_fee();

            fee_payer_account.checked_sub_lamports(fee).map_err(|_| {
                error_metrics.insufficient_funds += 1;
                TransactionError::InsufficientFundsForFee
            })?;

            let loaded_transaction = LoadedTransaction::new_for_failed_transaction_hotpath(
                *fee_payer,
                fee_payer_account,
                fee_details,
                compute_budget,
                loaded_accounts_data_size_limit.get(),
            );
            let execution_details = TransactionExecutionDetails::new_for_failed_transaction_hotpath(
                TransactionError::InstructionError(
                    0,
                    InstructionError::ComputationalBudgetExceeded,
                ),
            );

            let executed_tx = ExecutedTransaction {
                loaded_transaction,
                execution_details,
                programs_modified_by_tx: HashMap::new(),
            };

            processed_transactions_count += 1;
            processed_non_vote_transactions_count += 1;
            signature_count += u64::from(message.header().num_required_signatures);

            error_metrics.instruction_error += 1;

            Ok(ProcessedTransaction::Executed(Box::new(executed_tx)))
        })
        .collect();

    let processed_counts = ProcessedTransactionCounts {
        processed_transactions_count,
        processed_non_vote_transactions_count,
        // All of the transactions are expected to fail process, or later, during the execution.
        processed_with_successful_result_count: 0,
        signature_count,
    };

    LoadAndExecuteTransactionsOutput {
        processing_results,
        processed_counts,
        balance_collector,
    }
}

